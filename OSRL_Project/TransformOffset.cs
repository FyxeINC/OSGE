public class TransformOffset
{
  public int Top,Bottom,Left,Right;

  public TransformOffset()
  {
    Top = 0;
    Bottom = 0;
    Left = 0;
    Right = 0;
  }

  public TransformOffset(int top, int bottom, int left, int right)
  {
    Top = top;
    Bottom = bottom;
    Left = left;
    Right = right;
  }
}